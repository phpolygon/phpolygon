<?php

declare(strict_types=1);

namespace PHPolygon\Build\Mesh;

use PHPolygon\Geometry\MeshData;

/**
 * Emits a deterministic PHP class file from a MeshData instance.
 *
 * The generated class lives in `PHPolygon\Geometry\Generated` and exposes
 * a single static `generate(): MeshData` method that returns the embedded
 * vertex / normal / uv / index buffers verbatim. This matches the engine's
 * other mesh generators (BoxMesh, CylinderMesh, etc.) and integrates with
 * MeshRegistry without any runtime parsing.
 *
 * Output format goals:
 *   - reproducible: same MeshData → identical output (line for line)
 *   - readable:     buffers wrapped at a fixed column count, easy to diff
 *   - reviewable:   header comment names the source SVG and conversion
 *                   parameters so the file traces back to its origin
 *
 * The result is committable PHP code, not a runtime asset import path.
 */
final class PhpMeshGenerator
{
    /** Floats per line in the emitted buffer arrays (compactness vs. diffability). */
    private const VERTICES_PER_LINE = 9;   // 3 verts × xyz
    private const NORMALS_PER_LINE  = 9;
    private const UVS_PER_LINE      = 12;  // 6 pairs
    private const INDICES_PER_LINE  = 12;  // 4 triangles
    private const FLOAT_PRECISION   = 6;

    public function generate(
        MeshData $mesh,
        string $className,
        string $namespace = 'PHPolygon\\Geometry\\Generated',
        ?string $sourceComment = null,
    ): string {
        $classDoc = $sourceComment ?? "Auto-generated by PhpMeshGenerator.";
        // Indent the doc-block content correctly even when it spans lines.
        $classDocLines = array_map(
            static fn(string $line): string => " * " . $line,
            explode("\n", $classDoc),
        );
        $classDocBlock = implode("\n", $classDocLines);

        $vertices = $this->floatBlock($mesh->vertices, self::VERTICES_PER_LINE);
        $normals  = $this->floatBlock($mesh->normals,  self::NORMALS_PER_LINE);
        $uvs      = $this->floatBlock($mesh->uvs,      self::UVS_PER_LINE);
        $indices  = $this->intBlock($mesh->indices,    self::INDICES_PER_LINE);

        $vertexCount   = $mesh->vertexCount();
        $triangleCount = $mesh->triangleCount();

        return <<<PHP
            <?php

            declare(strict_types=1);

            namespace {$namespace};

            use PHPolygon\\Geometry\\MeshData;

            /**
            {$classDocBlock}
             *
             * Vertices:  {$vertexCount}
             * Triangles: {$triangleCount}
             *
             * DO NOT EDIT BY HAND. Re-generate via `bin/svg2mesh` if the
             * source SVG or extrusion parameters change.
             */
            final class {$className}
            {
                public static function generate(): MeshData
                {
                    return new MeshData(
                        vertices: [
            {$vertices}
                        ],
                        normals: [
            {$normals}
                        ],
                        uvs: [
            {$uvs}
                        ],
                        indices: [
            {$indices}
                        ],
                    );
                }
            }

            PHP;
    }

    /**
     * @param float[] $values
     */
    private function floatBlock(array $values, int $perLine): string
    {
        $lines = [];
        $chunks = array_chunk($values, max(1, $perLine));
        foreach ($chunks as $chunk) {
            $cells = array_map(
                fn(float $v): string => $this->formatFloat($v),
                $chunk,
            );
            $lines[] = '                ' . implode(', ', $cells) . ',';
        }
        return implode("\n", $lines);
    }

    /**
     * @param int[] $values
     */
    private function intBlock(array $values, int $perLine): string
    {
        $lines = [];
        $chunks = array_chunk($values, max(1, $perLine));
        foreach ($chunks as $chunk) {
            $cells = array_map(static fn(int $v): string => (string)$v, $chunk);
            $lines[] = '                ' . implode(', ', $cells) . ',';
        }
        return implode("\n", $lines);
    }

    private function formatFloat(float $v): string
    {
        // -0.0 prints as "0", matches positive-zero in diffs.
        if ($v == 0.0) return '0.0';
        $formatted = number_format($v, self::FLOAT_PRECISION, '.', '');
        // Trim trailing zeros but keep one decimal for clarity.
        $formatted = rtrim($formatted, '0');
        if (str_ends_with($formatted, '.')) $formatted .= '0';
        return $formatted;
    }
}
